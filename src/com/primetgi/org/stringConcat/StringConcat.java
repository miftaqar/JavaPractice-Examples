package com.primetgi.org.stringConcat;

public class StringConcat {

	/*
	 * Below Example demonstrates appending the string at the beginning of the
	 * another string
	 */
	public static void main(String[] args) {

		String s1 = "101";
		s1 = "'abx" + s1 + "'";

		System.out.println("String Appended at Beginning: " + s1);

	}

}
